    /**
     * 生成中文名称
     *
     * @return
     */
    public static String getChineseName(String s) {
        /**
         * 生成姓名的男还是女。
         */
        String name_sex = "";

        int index = getNum(0, surname.length() - 1);
        String first = surname.substring(index, index + 1);
        int sex = getNum(0, 1);
        String str = boyName;
        int length = boyName.length();
        if (sex == 0) {
            str = girlName;
            length = girlName.length();
            name_sex = "女";
        } else {
            name_sex = "男";
        }
        index = getNum(0, length - 1);
        String second = str.substring(index, index + 1);
        int hasThird = getNum(0, 1);
        String third = "";
        if (hasThird == 1) {
            index = getNum(0, length - 1);
            third = str.substring(index, index + 1);
        }
        return name_sex + ":" + first + second + third;
    }

